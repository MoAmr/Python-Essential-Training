# types in Python.
# type is a built-in function that returns the type of the value

var1 = 7
var2 = 7.0
var3 = '7.0'
var4 = True
var5 = None
print('var1 is {}'.format(var1))
print('var2 is {}'.format(var2))
print('var3 is {}'.format(var3))
print('var4 is {}'.format(var4))
print('var5 is {}'.format(var5))
print(type(var1))
print(type(var2))
print(type(var3))
print(type(var4))
print(type(var5))
